def GET_NAJIA_FOR_HEXAGRAM(gong: str, hexagram: str, yao_position: str) -> Optional[str]:
    """
    获取指定卦象指定爻的纳甲信息

    Args:
        gong: 宫名（乾宫、坎宫等）
        hexagram: 卦名
        yao_position: 爻位（初爻、二爻、三爻、四爻、五爻、上爻）

    Returns:
        纳甲字符串（如"甲子水"），未找到返回None
    """
    if gong not in HEXAGRAM_NAJIA:
        return None
    if hexagram not in HEXAGRAM_NAJIA[gong]:
        return None
    return HEXAGRAM_NAJIA[gong][hexagram].get(yao_position)


def GET_LIUQIN_RELATION(gong: str, wuxing: str) -> Optional[str]:
    """
    根据宫位和爻五行获取六亲关系

    Args:
        gong: 宫名
        wuxing: 五行（金、木、水、火、土）

    Returns:
        六亲名称（父母、兄弟、子孙、妻财、官鬼），未找到返回None
    """
    if gong not in LIUQIN_BY_GONG_WUXING:
        return None
    return LIUQIN_BY_GONG_WUXING[gong].get(wuxing)


def GET_WUXING_FROM_NAJIA(najia: str) -> str:
    """
    从纳甲字符串提取五行

    Args:
        najia: 纳甲字符串（如"甲子水"）

    Returns:
        五行名称
    """
    wuxing = najia[-1]
    return wuxing


def GET_GANZHI_FROM_NAJIA(najia: str) -> str:
    """
    从纳甲字符串提取干支

    Args:
        najia: 纳甲字符串（如"甲子水"）

    Returns:
        干支字符串（如"甲子"）
    """
    return najia[:-1]
